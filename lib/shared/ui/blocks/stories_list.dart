  @override
  Widget build(BuildContext context) {
    final paddingTop = MediaQuery.of(context).padding.top;
    final screenWidth = MediaQuery.of(context).size.width;
    final currentStory = currentStories[currentInnerStoryIndex];
    final button = _preloadedStories[currentStory.id ?? -1]?.button;

    return Scaffold(
      backgroundColor: Colors.black,
      body: Transform.scale(
        scale: _calculateScale(),
        child: Opacity(
          opacity: _calculateOpacity(),
          child: Stack(
            children: [
              _buildPageView(), // –ó–∞–º–µ–Ω—è–µ–º —Å—Ç–∞—Ä—ã–π PageView –Ω–∞ –Ω–æ–≤—ã–π —Å —ç—Ñ—Ñ–µ–∫—Ç–æ–º –∫—É–±–∞
              // GestureDetector –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ —Ç–∞–ø–æ–≤
              Positioned.fill(
                child: GestureDetector(
                  onTapDown: (details) {
                    _progressController.stop();
                  },
                  onTapUp: (details) {
                    if (!_isDragging) {
                      if (details.globalPosition.dx < screenWidth / 2) {
                        handlePreviousStory();
                      } else {
                        handleNextStory();
                      }
                      _progressController.forward();
                    }
                  },
                  onTapCancel: () {
                    if (!_isDragging) {
                      _progressController.forward();
                    }
                  },
                  onVerticalDragStart: (_) {
                    _isDragging = true;
                    _progressController.stop();
                  },
                  onVerticalDragUpdate: (details) {
                    // –¢–µ–ø–µ—Ä—å –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º —Ç–æ–ª—å–∫–æ —Å–≤–∞–π–ø –≤–≤–µ—Ä—Ö (–æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ)
                    if (details.primaryDelta! > 0) return;

                    setState(() {
                      _dragOffset += details.primaryDelta!;
                    });
                  },
                  onVerticalDragEnd: (details) {
                    _isDragging = false;

                    // –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–∫–æ—Ä–æ—Å—Ç—å —Å–≤–∞–π–ø–∞ –≤–≤–µ—Ä—Ö (–æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ)
                    if (_dragOffset.abs() > 100 ||
                        (details.primaryVelocity ?? 0) < -300) {
                      // –ó–∞–∫—Ä—ã–≤–∞–µ–º –∏—Å—Ç–æ—Ä–∏—é –µ—Å–ª–∏ –¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –æ—Ç—Ç—è–Ω—É–ª–∏ –∏–ª–∏ –±—ã—Å—Ç—Ä–æ —Å–≤–∞–π–ø–Ω—É–ª–∏ –≤–≤–µ—Ä—Ö
                      Navigator.of(context).pop();
                    } else {
                      // –í–æ–∑–≤—Ä–∞—â–∞–µ–º –Ω–∞ –º–µ—Å—Ç–æ —Å –∞–Ω–∏–º–∞—Ü–∏–µ–π
                      setState(() {
                        _dragOffset = 0;
                      });
                      _progressController.forward();
                    }
                  },
                  onHorizontalDragEnd: (details) {
                    if (details.primaryVelocity == null) return;

                    // –ï—Å–ª–∏ —Å–∫–æ—Ä–æ—Å—Ç—å —Å–≤–∞–π–ø–∞ –º–µ–Ω—å—à–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω–æ–≥–æ –ø–æ—Ä–æ–≥–∞, –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º
                    if (details.primaryVelocity!.abs() < 200) return;

                    if (details.primaryVelocity! > 0) {
                      // –°–≤–∞–π–ø –≤–ø—Ä–∞–≤–æ - –∫ –ø—Ä–µ–¥—ã–¥—É—â–µ–π –≥—Ä—É–ø–ø–µ
                      if (currentStoryIndex > 0) {
                        _uiAnimationController.forward();
                        final prevStoryIndex = currentStoryIndex - 1;
                        final prevStories =
                            widget.stories[prevStoryIndex].stories ?? [];

                        _groupPageController
                            .animateToPage(
                          prevStoryIndex,
                          duration: const Duration(milliseconds: 300),
                          curve: Curves.easeInOut,
                        )
                            .then((_) {
                          setState(() {
                            currentStoryIndex = prevStoryIndex;
                            currentStories = prevStories;
                            currentInnerStoryIndex = 0;
                          });
                          _pageController = PageController(initialPage: 0);
                          _progressController.reset();
                          _progressController.forward();
                          _uiAnimationController.reverse();
                        });
                      }
                    } else {
                      // –°–≤–∞–π–ø –≤–ª–µ–≤–æ - –∫ —Å–ª–µ–¥—É—é—â–µ–π –≥—Ä—É–ø–ø–µ
                      if (currentStoryIndex < widget.stories.length - 1) {
                        _uiAnimationController.forward();
                        final nextStoryIndex = currentStoryIndex + 1;
                        final nextStories =
                            widget.stories[nextStoryIndex].stories ?? [];

                        _groupPageController
                            .animateToPage(
                          nextStoryIndex,
                          duration: const Duration(milliseconds: 300),
                          curve: Curves.easeInOut,
                        )
                            .then((_) {
                          setState(() {
                            currentStoryIndex = nextStoryIndex;
                            currentStories = nextStories;
                            currentInnerStoryIndex = 0;
                          });
                          _pageController = PageController(initialPage: 0);
                          _progressController.reset();
                          _progressController.forward();
                          _uiAnimationController.reverse();
                        });
                      }
                    }
                  },
                ),
              ),
              // –í–µ—Ä—Ö–Ω—è—è –ø–∞–Ω–µ–ª—å —Å –ø—Ä–æ–≥—Ä–µ—Å—Å–æ–º –∏ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π
              FadeTransition(
                opacity: _uiFadeAnimation,
                child: Container(
                  width: double.infinity,
                  padding: EdgeInsets.only(
                    top: paddingTop + 8,
                    left: 16,
                    right: 16,
                  ),
                  child: Column(
                    children: [
                      Row(
                        children: List.generate(
                          currentStories.length,
                          (index) => Expanded(
                            child: Container(
                              height: 2,
                              margin: const EdgeInsets.symmetric(horizontal: 2),
                              child: ValueListenableBuilder<double>(
                                valueListenable: _progressController,
                                builder: (context, value, child) {
                                  return LinearProgressIndicator(
                                    value: index < currentInnerStoryIndex
                                        ? 1.0
                                        : (index == currentInnerStoryIndex
                                            ? value
                                            : 0.0),
                                    backgroundColor:
                                        Colors.grey.withOpacity(0.5),
                                    valueColor:
                                        const AlwaysStoppedAnimation<Color>(
                                            Colors.white),
                                  );
                                },
                              ),
                            ),
                          ),
                        ),
                      ),
                      const SizedBox(height: 16),
                      Row(
                        mainAxisAlignment: MainAxisAlignment.spaceBetween,
                        children: [
                          Row(
                            children: [
                              Container(
                                width: 32,
                                height: 32,
                                decoration: BoxDecoration(
                                  shape: BoxShape.circle,
                                  image: DecorationImage(
                                    image: _getImageProvider(
                                      widget.stories[currentStoryIndex]
                                          .previewImage,
                                    ),
                                    fit: BoxFit.cover,
                                  ),
                                ),
                              ),
                              const SizedBox(width: 8),
                              Text(
                                widget.stories[currentStoryIndex].name ?? '',
                                style: const TextStyle(
                                  color: Colors.white,
                                  fontSize: 16,
                                  fontWeight: FontWeight.w600,
                                ),
                              ),
                            ],
                          ),
                          IconButton(
                            icon: const Icon(Icons.close, color: Colors.white),
                            onPressed: () => Navigator.of(context).pop(),
                            padding: EdgeInsets.zero,
                            constraints: const BoxConstraints(),
                          ),
                        ],
                      ),
                    ],
                  ),
                ),
              ),
              // –ö–Ω–æ–ø–∫–∞ –≤–Ω–∏–∑—É —ç–∫—Ä–∞–Ω–∞
              if (button != null)
                Positioned(
                  bottom: MediaQuery.of(context).padding.bottom + 20,
                  left: 16,
                  right: 16,
                  child: FadeTransition(
                    opacity: _uiFadeAnimation,
                    child: CustomButton(
                      button: button,
                      type: ButtonType.bordered,
                      isFullWidth: true,
                      backgroundColor: Colors.white,
                      textColor: Colors.black,
                      onPressed: () {
                        Navigator.of(context).pop();
                        ButtonNavigationHandler.handleNavigation(
                            context, ref, button);
                      },
                    ),
                  ),
                ),
            ],
          ),
        ),
      ),
    );
  }

