                                    scale: scale,
                                    child: child,
                                  );
                                },
                                child: Builder(
                                  builder: (context) {
                                    final isViewed =
                                        _viewedStatusCache[story.id] ?? false;
                                    return Container(
                                      width: 68,
                                      height: 68,
                                      decoration: BoxDecoration(
                                        shape: BoxShape.circle,
                                        gradient: LinearGradient(
                                          begin: Alignment.topLeft,
                                          end: Alignment.bottomRight,
                                          colors: isViewed
                                              ? [
                                                  AppColors.primary,
                                                  AppColors.primary
