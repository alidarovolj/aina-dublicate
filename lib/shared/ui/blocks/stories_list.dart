                                                  AppColors.secondary,
                                                  AppColors.secondary
                                                      .withOpacity(0.8),
                                                ],
                                        ),
                                        boxShadow: [
                                          BoxShadow(
                                            color: (isViewed
                                                    ? AppColors.primary
                                                    : AppColors.secondary)
