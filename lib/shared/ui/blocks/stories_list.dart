          isServerError: true,
          icon: Icons.warning_amber_rounded,
        );
      },
      data: (storiesList) {
        return FutureBuilder<List<Story>>(
          future: _getStaticStoriesWithStatus(),
          builder: (context, staticSnapshot) {
            final staticStories = staticSnapshot.data ?? _getStaticStories();
